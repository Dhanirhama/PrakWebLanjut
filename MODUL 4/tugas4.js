// Membuat array kosong untuk menampung bilangan genap
let evenNumber = [];

// Mengisi array dengan bilangan genap dari 1 hingga 500
for (let i = 1; i <= 500; i++) {
  if (i % 2 === 0) {
    evenNumber.push(i);
  }
}

// Menampilkan array bilangan genap
